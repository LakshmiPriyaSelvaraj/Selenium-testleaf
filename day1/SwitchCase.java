package week1.day1;

public class SwitchCase {

	public static void main(String[] args) {
		String browsertype = "Edge";
		switch(browsertype){
		case "Chrome" : System.out.println("Chrome browser");break;
		case "FF" : System.out.println("FF browser");break;
		case "IE" : System.out.println("IE browser");break;
		case "Edge" : System.out.println("Edge browser");break;
		}

	}

}
